     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request, User $user)
    {
        $request->validate([
            'name' => 'required',
            'email' => 'required',
            'role' => 'required',
            'password' => 'required',
            'job' => 'required',
        ]);

        $input = $request->all();
        User::create($input);

        return redirect()->route('userRegister')
                        ->with('success','User Berhasil Ditambah.');
    }

